    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        AgendaVO vo = new AgendaVO();

        vo.setAgenda_cliente(Integer.parseInt(jTextField_Cliente.getText()));
        vo.setAgenda_funcionario(Integer.parseInt(jTextField_Funcionario.getText()));
        vo.setAgenda_data(jTextField_data.getText());
        vo.setAgenda_inicio(jTextField_inicio.getText());
        vo.setAgenda_fim(jTextField_fim.getText());
        vo.setAgenda_status(jTextField_status.getText());

        AgendaDAO dao = new AgendaDAO();

        if (jTextField_ID.getText().isEmpty()) {
            try {
                System.out.println("criando novo agendamento");
                dao.criar(vo);
            } catch (Exception e) {
                System.out.println(e);
            }

        } else {
            try {
                System.out.println("alterando agendamento");
                vo.setAgenda_id(Integer.parseInt(jTextField_ID.getText()));
                dao.alterar(vo);
            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

