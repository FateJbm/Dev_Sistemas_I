    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        ClienteVO vo = new ClienteVO();

        vo.setCliente_cpf(jTextFieldClienteCPF.getText());
        vo.setCliente_nome(jTextFieldClienteNome.getText());
        vo.setCliente_rg(jTextFieldClienteRG.getText());
        vo.setCliente_endereco(jTextFieldClienteEndereco.getText());
        vo.setCliente_cidade(jTextFieldClienteCidade.getText());
        vo.setCliente_estado(jTextFieldClienteEstado.getText());
        vo.setCliente_email(jTextFieldClienteEmail.getText());
        vo.setCliente_fone(Integer.parseInt(jTextFieldClienteFone.getText()));

        ClienteDAO dao = new ClienteDAO();

        if ((vo.getCliente_email().length() < 8) || !vo.getCliente_email().contains("@") || !vo.getCliente_email().contains(".")) {
            JOptionPane.showInternalMessageDialog(rootPane, "Eggs are not supposed to be green.", "Inane warning", JOptionPane.WARNING_MESSAGE);
        } else if (jTextFieldClienteID.getText().isEmpty()) {
            try {
                System.out.println("criando novo cliente");
                dao.criar(vo);
                tableModel.setQuery(QUERY_DEFAULT);
            } catch (Exception e) {
                System.out.println(e);
            }

        } else {
            try {
                System.out.println("alterando cliente");
                vo.setCliente_id(Integer.parseInt(jTextFieldClienteID.getText()));
                dao.alterar(vo);
                tableModel.setQuery(QUERY_DEFAULT);
            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        ClienteVO vo = new ClienteVO();
        ClienteDAO dao = new ClienteDAO();
        vo.setCliente_id(Integer.parseInt(jTextFieldClienteID.getText()));
        int confirma = JOptionPane.showConfirmDialog(rootPane, "Confirma a exclusão?");
        if (confirma == 0) {
            try {
                dao.excluir(vo);
                tableModel.setQuery(QUERY_DEFAULT);
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
        }
        jTextFieldClienteID.setText("");
        jTextFieldClienteCPF.setText("");
        jTextFieldClienteNome.setText("");
        jTextFieldClienteRG.setText("");
        jTextFieldClienteEndereco.setText("");
        jTextFieldClienteCidade.setText("");
        jTextFieldClienteEstado.setText("");
        jTextFieldClienteEmail.setText("");
        jTextFieldClienteFone.setText("");
        jTextFieldClienteDataCadastro.setText("");
    }//GEN-LAST:event_jButtonExcluirActionPerformed

