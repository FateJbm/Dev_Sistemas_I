    /**
     * AÃ§Ã£o do BotÃ£o Excluir para o agendamento selecionado na tabela Solicita
     * confirmaÃ§Ã£o de exclusÃ£o
     */
    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        AgendaVO vo = new AgendaVO();
        AgendaDAO dao = new AgendaDAO();

        int row = jTable1.getSelectedRow(); //Use getSelectedRows se vc permite seleÃ§Ã£o mÃºltipla

        vo.setAgenda_cliente(HIDE_ON_CLOSE);

        vo.setAgenda_id((int) jTable1.getValueAt(row, 2));

        int confirma = JOptionPane.showConfirmDialog(rootPane, "Confirma a exclusÃ£o?");
        if (confirma == 0) {
            try {
                dao.excluir(vo);
                tableModel.setQuery(QUERY_DEFAULT);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

